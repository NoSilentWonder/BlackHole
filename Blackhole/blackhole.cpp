// CONSTRUCTOR ///////////////////////////////////////////////////////////////////////////////////////
cBlackHole::cBlackHole()
{
	noofthreads = 2;
	time_taken = 0;
}
//////////////////////////////////////////////////////////////////////////////////////////////////////

// DESTRUCTOR ////////////////////////////////////////////////////////////////////////////////////////
cBlackHole::~cBlackHole()
{
}
//////////////////////////////////////////////////////////////////////////////////////////////////////

// SETUP FUNCTION ////////////////////////////////////////////////////////////////////////////////////
void cBlackHole::GameSetUp()
{
	m_sceneManager.push(*new cTitleScene);
	m_sceneManager.m_activeScene->setup();
}
//////////////////////////////////////////////////////////////////////////////////////////////////////

// UPDATE FUNCTION ///////////////////////////////////////////////////////////////////////////////////
void cBlackHole::Update() 
{   
	m_sceneManager.m_activeScene->update(); 
} 
//////////////////////////////////////////////////////////////////////////////////////////////////////

// RENDER FUNCTION ///////////////////////////////////////////////////////////////////////////////////
void cBlackHole::Render() 
{   
	m_sceneManager.m_activeScene->render(); 
}
