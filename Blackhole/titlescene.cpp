// SETUP FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cTitleScene::setup()
{
	g_game.m_background_sprite.SetDisplayBitmap("Images/title.bmp");
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//UPDATE FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cTitleScene::update()
{
	if(g_game.m_keys[13])
	{
		g_game.m_keys[13] = false;
		g_game.m_sceneManager.push(*new cMenuScene);
		g_game.m_sceneManager.m_activeScene->setup();
	}
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//RENDER FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cTitleScene::render()
{
	g_game.m_background_sprite.Render();
}
