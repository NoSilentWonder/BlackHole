// DESTRUCTOR ////////////////////////////////////////////////////////////////////////////////////////////////////
cMenuScene::~cMenuScene()
{
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// SETUP FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cMenuScene::setup()
{
	g_game.m_background_sprite.SetDisplayBitmap("Images/start.bmp");
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//UPDATE FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cMenuScene::update()
{
	if(g_game.m_keys[13])
	{
		g_game.m_keys[13] = false;
		g_game.m_sceneManager.push(*new cPlayScene);
		g_game.m_sceneManager.m_activeScene->setup();
	}
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//RENDER FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cMenuScene::render()
{
	g_game.m_background_sprite.Render();
}
