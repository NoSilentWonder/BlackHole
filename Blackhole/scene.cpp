// CONSTRUCTOR ///////////////////////////////////////////////////////////////////////////////////////////////////
cSceneManager::cSceneManager()
{
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// DESTRUCTOR ////////////////////////////////////////////////////////////////////////////////////////////////////
cSceneManager::~cSceneManager()
{
	while(!m_scenes.empty())
	{
		delete m_scenes.top();
		m_scenes.pop();
	}
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// PUSH FUNCTION /////////////////////////////////////////////////////////////////////////////////////////////////
void cSceneManager::push(cScene &s)
{
	m_scenes.push(&s);
	m_activeScene = m_scenes.top();
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// POP FUNCTION //////////////////////////////////////////////////////////////////////////////////////////////////
void cSceneManager::pop()
{
	delete m_scenes.top();
	m_scenes.pop();
	m_activeScene = m_scenes.top();
}
