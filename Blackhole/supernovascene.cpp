// SETUP FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cSupernovaScene::setup()
{
	g_game.m_background_sprite.SetDisplayBitmap("Images/supernova.bmp");
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//UPDATE FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cSupernovaScene::update()
{
	if(g_game.m_keys[13])
	{
		g_game.m_keys[13] = false;
		g_game.m_sceneManager.pop();
		g_game.m_sceneManager.pop();
		g_game.m_sceneManager.m_activeScene->setup();
	}
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//RENDER FUNCTION ////////////////////////////////////////////////////////////////////////////////////////////////
void cSupernovaScene::render()
{
	g_game.m_background_sprite.Render();
}
